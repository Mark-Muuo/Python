#Absolute abs()
print(abs(-20))
print(abs(+23))
print(abs(19))

#power pow()
print(pow(2,2))
print(pow(2,3))
print(pow(2,4))

#rounding off round()
print(round(10.2))
print(round(10.7))

#truncate(removing decimal)
import math
print(math.trunc(10.2))
print(math.trunc(10.7))

#math.ceil  approximates to nearest whole number
print(math.ceil(10.2))
print(math.ceil(10.7))